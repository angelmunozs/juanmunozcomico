						<p>
							Llegué a Madrid  en un 124 con un destornillador en la ventanilla, que siempre andaba floja la pobre, y con un sueño en el maletero: hacer humor.
						</p>
					</blockquote>
				</div>
				<div class="row">
					<p> 
						Hoy, treinta años después, ya no existe el 124 ni el bendito destornillador, pero permanece intacto el sueño del maletero. Permanece intacta la ilusión por comunicarme con el público utilizando el humor para devolverles momentos, situaciones y personajes que son de ellos, que les pertenecen; personajes que son ellos mismos, porque los cómicos <b>lo debemos todo a nuestro público</b>. Y no es una frase hecha, quiero decir que me apropio de detalles, de frases, de rostros y de guiños, y que cuando ese material lo elaboro y se lo devuelvo a la gente en forma de personaje se produce siempre una extraña mutación: el personaje existe y la mayoría de las personas no saben que existe gracias a ellos. Procuro hacer personajes de verdad, con sus limitaciones, sus sueños, sus contradicciones y sus defectos. Como yo. Como mi público.
					</p>
					<p> 
						En mis espectáculos seguiré jugando con el humor, amasando parodias y afilando imitaciones. Es curioso, pero se podría decir que copio para ser más original, que imito para ser más auténtico, que parodio porque sé que exprimiendo las parodias se obtienen siempre unas gotas de verdad. Esa es mi intención.
					</p>
				</div>
				<div class="row">
					<blockquote>
						<p>
						Para mí el humor es una forma de ser, un espejo gigante y entrañable en el que cuando te miras te devuelves a ti mismo una sonrisa tierna y te conoces un poco más.
						</p>
					</blockquote>
				</div>
				<div class="row">
					<p>					
						 No soy pretencioso; pretenciosa es la realidad, que muchas veces compite conmigo en el absurdo. Siempre me he tomado muy en serio el humor, pero creo que, afortunadamente, es el humor el que me ha ido tomando en serio a mí. Me gusta el humor hecho en serio, el humor que es como una revolución eternamente pendiente, el humor constructivo, el humor que nos salva, el que viene a ser como un torpedo en la línea de flotación de la vida; el humor que nos hace ser ricos de espíritu, el que nos ayuda a descubrirnos a nosotros mismos, el que nos hace más libres.
					</p>
					<p> 
						En mis trabajos quiero que mis espectadores sueñen, <b>que soñéis</b>, que os descubráis a vosotros mismos un poco más, que os miréis en esos personajes que siguen creciendo y evolucionando conmigo y con vosotros. Porque también sois vosotros quienes me habéis ayudado a componerlos. Vosotros estáis ahí, en ellos.
					</p>
