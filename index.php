			<!-- Sección 2: Biografía -->
			<div id="section-biografia" class="section">

				<div class="section-title title-odd">
					Biografía
				</div>

				<div class="row">
					<div class="col-xs-12 col-lg-6">
						<p>
							Juan Antonio Muñoz Pérez nació en Barcelona en 1965. 
						</p>
						<p>
							El colegio fue su primer escenario. Bajo el nombre artístico de <i>Super Agente Billy</i> obtiene un éxito enorme imitando a los profesores y representando pequeñas obras de humor. 
						</p>
						<p>
							Su triunfo excede las fronteras de los Salesianos y comienza a actuar en bodas y a realizar parodias e imitaciones de cantantes y famosos en diversos programas infantiles de Radio Reloj, Radio Miramar y Radio Poble Sec, como <i>Los chicos de Mario</i> o <i>Niñolandia</i>. Tenía claro que quería ser artista. Era solo un chaval y ya contaba con un montón de actuaciones a sus espaldas por pubs, discotecas, plazas de pueblos, salas de fiesta y locales de todo tipo.
						</p>
						<p>
							El mundo del espectáculo se fija en aquel monstruo de la escena y una sala lo ficha por 1.000 pesetas y la cena, naturalmente. Pero Juan no se duerme. A pesar de saborear las mieles del éxito, siente que necesita nuevos horizontes. Con el nombre de <i>Sergio TV</i>, graba una cinta de chistes que obtiene un rotundo éxito en las gasolineras.
						</p>
						<p>
							Y, por fin, un importante grupo empresarial le ofrece un suculento contrato. A punto ya de firmar, lo llaman a filas. Juan no lo duda y se inclina por la amable invitación que le hace el Ejército, tan necesitado de humoristas. Ante los mandos, Juan confiesa que es cómico, con lo que disfrutó de una mili maravillosa hasta que se le acabó el repertorio. Uno de sus momentos estelares fue su actuación en el polvorín. 
						</p>
						<p>
							A punto ya de terminar la mili, el Cabo le informa de que le ha salido un competidor. Juan va a verlo y descubre al que sería su <b>futuro compañero en Cruz y Raya</b>. Hablan de hacer algo juntos en el futuro.
						</p>
					</div>
					<div class="col-xs-12 col-lg-6">
						<p>
							Y lo hicieron. En Madrid montan <i>60 personajes en busca de humor</i>. Juan y su nuevo socio realizan innumerables actuaciones por las Salas de Fiesta de la época mientras comienzan a colaborar en diversos programas de Radio: <i>Viva la gente divertida</i>, en Antena 3; <i>El banquillo</i>, con Luis del Olmo, y <i>La bisagra</i>, con Sardá. Y siempre con éxito. Ya se llaman Cruz y Raya y, tras su show en Caribiana, comienzan a actuar en Windsor, donde permanecen dos años consecutivos y donde despiertan el interés de Hugo Stuven, que confía ciegamente en ellos y se los lleva a TVE.
						</p>
						<p>
							<i>Pero esto qué es</i> supuso su consagración definitiva en televisión. Tras su paso por <i>Tutti fruti</i>, en Tele 5, regresan a TVE donde participan en <i>Hola Rafaella</i> y donde, desde el 93, realizan programas de humor y especiales que han obtenido el respaldo mayoritario del público y donde Juan ha creado personajes entrañables como Morito, Juan de Dios, el viejo Ramón, Maruja, Canduterio...
						</p>
						<p>
							Siempre le ha gustado el cine. Ha dirigido dos largometrajes, <i>¡Ja me maaten...!</i> y <i>Ekipo Ja</i>. Actualmente prepara el tercero, <i>Piratas de los Madriles</i>, una gran aventura de piratas. Como actor ha intervenido en <i>Trileros</i>, <i>Desde que amanece apetece</i>, <i>Clara no es nombre de mujer</i>, <i>Holmes Madrid Suite de Garci</i>, y otras muchas, hasta un total de 16. 
						</p>
						<p>
							Además, la voz de Shrek es la suya, también se ha paseado por los escenarios con una comedia del arte <i>Nazionale</i> y una versión circense del Quijote, <i>En un lugar de la magia</i>, en el Price.
						</p>
						<p>
							La música es otra de sus pasiones que no para de cultivar, especialmente con el piano y la guitarra. Ahora está de gira con un nuevo espectáculo de humor y se encuentra escribiendo guiones para sus nuevos proyectos de televisión. Su ilusión por comunicarse con el público sigue intacta. Y es que Juan y el humor siempre se han tomado muy en serio.
						</p>
					</div>
				</div>

